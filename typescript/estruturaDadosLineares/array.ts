// Em TypeScript, você pode declarar e inicializar um array usando a sintaxe de colchetes []

let numeros: number[] = [1, 2, 3, 4, 5];


//Você também pode usar a classe Array para criar um array em TypeScript, passando os elementos como argumentos do construtor. Aqui está um exemplo:
let nomes = new Array("Ana", "Bia", "Carlos", "Daniel");
//Este array chamado nomes tem quatro elementos, que são strings.

// Você pode acessar e alterar os elementos do array usando um índice baseado em zero. 
let primeiro = numeros[0]; // primeiro é assignado ao valor 1